number1 = 10
number2 = 5

sum_result = number1 + number2
diff_result = number1 - number2
prod_result = number1 * number2

print(f"The addition of {number1} and {number2} is: {number1} + {number2} = {sum_result}")
print(f"The subtraction of {number1} and {number2} is: {number1} - {number2} = {diff_result}")
print(f"The multiplication of {number1} and {number2} is: {number1} * {number2} = {prod_result}")
